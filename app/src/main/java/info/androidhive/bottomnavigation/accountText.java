package info.androidhive.bottomnavigation;

public class accountText{

    String mainString;
    String aboutMainString;

    public accountText(String mainString,String aboutMainString){
        this.mainString = mainString;
        this.aboutMainString = aboutMainString;
    }

    public String getMainString(){
        return mainString;
    }

    public String getAboutMainString(){
        return aboutMainString;
    }
}

